"""
How can you assign the following rhyme to a single variable while preserving 
the line break?

A pirate I was meant to be!
Trim the sails and roam the sea!
"""

pirate_quote = """A pirate I was meant to be!
Trim the sails and roam the sea!"""

print(pirate_quote)
# A pirate I was meant to be!
# Trim the sails and roam the sea!

pirate_quote2 = "A pirate I was meant to be!\nTrim the sails and roam the sea!"

print(pirate_quote2)
# A pirate I was meant to be!
# Trim the sails and roam the sea!
